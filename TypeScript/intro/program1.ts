// variables in typescript
// variables are declared with let, const, var

// Syntax:
// let var_name: datatype = value;
export {};
let myName: string = "Richie";
let myAge: number = 25;
let isMarried: boolean = false;
let myHobbies: string[] = ["reading", "swimming", "coding"];
let myAttributes: Object = {
  name: "Richie",
  age: 25,
  occupation: "trainer",
};

console.log(myName);
console.log(myAge);
console.log(isMarried);
console.log(myHobbies);
console.log(myAttributes);
